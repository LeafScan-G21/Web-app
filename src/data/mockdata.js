export const getPostById = (id) => {
  return mockPosts.find((post) => post.id === id);
};

export const getPostsByPage = (page, postsPerPage = 5) => {
  const startIndex = (page - 1) * postsPerPage;
  const endIndex = startIndex + postsPerPage;
  return mockPosts.slice(startIndex, endIndex);
};

export const getTotalPages = (postsPerPage = 5) => {
  return Math.ceil(mockPosts.length / postsPerPage);
};

export const getRelatedPosts = (currentPostId, tags, plantName) => {
  return mockPosts
    .filter((post) => post.id !== currentPostId)
    .filter(
      (post) =>
        post.plantName.toLowerCase() === plantName.toLowerCase() ||
        post.tags.some((tag) => tags.includes(tag))
    )
    .slice(0, 3);
};
